package com.company;

public class operadores {
    public static void main(String[] args) {
        //operadores aritmeticos (+,-,*,/,%)
        int numero1= 15;
        int numero2= 2;
        /*
        Si quisieramos reutilizar alguna variable, no debemos volver
        a nombrar su tipo, arrojaria error de duplicada
         */
        int resultador = numero1 + numero2;
        System.out.println(resultador);
        int resultador1 = numero1 - numero2;
        System.out.println(resultador1);
        int resultador2 = numero1 * numero2;
        System.out.println(resultador2);
        int resultador3 = numero1 / numero2;
        System.out.println(resultador3);
        int resultador4 = numero1 % numero2;
        System.out.println(resultador4);

        //operadores logicos, relacion, comparacion etc(<,>,<=,>=,&&,||, ==, !=)
        //concatenacion(+)

    }
}
